////////////////////////////////////////////////////////////////////////////////
#ifndef SETTINGS_HPP
#define SETTINGS_HPP

////////////////////////////////////////////////////////////////////////////////
#include <QString>
#include <map>

////////////////////////////////////////////////////////////////////////////////
struct Uri
{
    QString host;
    int port = 0;
};

////////////////////////////////////////////////////////////////////////////////
struct Setting
{
    QString mode;
    QString autoconfig_url;
    QString ignore_hosts;
    std::map<QString, Uri> uris;
};

////////////////////////////////////////////////////////////////////////////////
struct Settings : public std::map<QString, Setting>
{
    static Settings from_file(const QString&);
    QString match(const Setting&);
};

////////////////////////////////////////////////////////////////////////////////
#endif
